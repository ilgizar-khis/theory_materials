"""
для выводы информации используется функция print,
которая принимает в скобках то, что вы выводите.
"""

print("Hello world!!!")  # пример вывода "Hello world"

"""
для получения ввода от пользователя используется функция input.
В скобках задаётся prompt -- подсказка для пользователя.

ввод пользователя принято сохранять в переменную.
"""

name = input("Enter your name: ")
# принимаем от пользователя его имя и сохраняем в переменную name
print("Hello", name)
